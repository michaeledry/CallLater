    private boolean validateForm() {
        boolean valid = true;

        String email = Femail.getText().toString();
        if (TextUtils.isEmpty(email)) {
            Femail.setError("Valid mail is Required.");
            valid = false;
        } else {
            Femail.setError(null);
        }

        String password = Fpass.getText().toString();
        if (TextUtils.isEmpty(password)) {
            Fpass.setError("Password is Required.");
            valid = false;
        } else {
            Fpass.setError(null);
        }

        return valid;
    }

